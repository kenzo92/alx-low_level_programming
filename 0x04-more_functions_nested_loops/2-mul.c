/**
 * mul - This function multiplies two integers
 * @a: Parameter that copies first integer value to be multiplied
 * @b: Parameter that copies second integer value to be multiplied
 *
 * Return: result of multiplication
 */
int mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);
}
